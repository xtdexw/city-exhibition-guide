/**
 * 根据分类获取内容
 */
export function getContentByCategory(category: string): ContentItem[] {
  return contentLibrary.filter(item => item.category === category)
}

/**
 * 根据标签搜索内容
 */
export function searchByTags(tags: string[]): ContentItem[] {
  return contentLibrary.filter(item =>
    tags.some(tag => item.tags.includes(tag))
  )
}

/**
 * 获取推荐内容（首页）
 */
export function getFeaturedContent(): ContentItem[] {
  return contentLibrary.slice(0, 4)
}
